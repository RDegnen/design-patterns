export default class GarageDoor {
  public up(): void {
    console.log('Garage door is going up')
  }

  public down(): void {
    console.log('Garage door is going down')
  }

  public stop(): void {
    console.log('Garage door stopped')
  }

  public lightOn(): void {
    console.log('Garage door light is on')
  }

  public lightOff(): void {
    console.log('Garage door light is off')
  }
}